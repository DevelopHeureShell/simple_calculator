### Functions used to proceed calculation.

def make_sum(number1, number2):
    return number1+number2

def make_substraction(number1,number2):
    return number1-number2

def make_multiplication(number1,number2):
    return number1*number2

def make_division(number1, number2):
    return number1/number2

def make_power(number, power):
    return number**power